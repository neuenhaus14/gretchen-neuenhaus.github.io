function drawChessboard(args) { // requires a parameter 
  var chessboard = ""
  
    for (let i = 0; i < args; i ++){
      for (let j = 0; j < args; j ++){
        if ((i + j) % 2 === 0){
         chessboard += " ";
        } else{
          chessboard += "#"      
        }
      }  
      chessboard += "\n"
    }
    console.log(chessboard)
  }; 


 
