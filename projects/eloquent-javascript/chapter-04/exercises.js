function range(start, end, step = 0) {
// range (2, 8) => [3, 4, 5, 6, 7, 8]
 
let output = [];
  
  if (start === end){
    return output;
  } else if (step < 0){
    return output;
  }

  if (step > 0){
    for (let i = start; i <= end; i+= step){
      output.push(i);
    }
  }else {
    for (let j = start; j <= end; j++){
      output.push(j);
    }
  }

  return output;

 }
   

